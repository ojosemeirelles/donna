import type { SessionSendPolicyConfig } from "./types.base.js";

export type MemoryBackend = "builtin" | "qmd";
export type MemoryCitationsMode = "auto" | "on" | "off";
export type MemoryQmdSearchMode = "query" | "search" | "vsearch";

export type MemoryConfig = {
  backend?: MemoryBackend;
  citations?: MemoryCitationsMode;
  qmd?: MemoryQmdConfig;
  // -------------------------------------------------------------------------
  // Donna 3-layer evolving memory system (identity + patterns + episodic)
  // -------------------------------------------------------------------------
  /** Enable the Donna native memory system. Default: false. */
  enabled?: boolean;
  /** Identity layer: persistent user identity and preferences. */
  identity?: { enabled?: boolean };
  /** Pattern layer: behavioral pattern tracking and weekly analysis. */
  patterns?: {
    enabled?: boolean;
    /** Days to retain raw events. Default: 28. */
    retentionDays?: number;
    /** Run pattern analysis every Sunday. Default: true. */
    analyzeOnSunday?: boolean;
  };
  /** Episodic layer: daily episode logs with search and summaries. */
  episodic?: {
    enabled?: boolean;
    /** Days to retain raw episode entries. Default: 30. */
    retentionDays?: number;
    /** Days to retain episode summaries. Default: 365. */
    summaryRetentionDays?: number;
  };
};

export type MemoryQmdConfig = {
  command?: string;
  mcporter?: MemoryQmdMcporterConfig;
  searchMode?: MemoryQmdSearchMode;
  includeDefaultMemory?: boolean;
  paths?: MemoryQmdIndexPath[];
  sessions?: MemoryQmdSessionConfig;
  update?: MemoryQmdUpdateConfig;
  limits?: MemoryQmdLimitsConfig;
  scope?: SessionSendPolicyConfig;
};

export type MemoryQmdMcporterConfig = {
  /**
   * Route QMD searches through mcporter (MCP runtime) instead of spawning `qmd` per query.
   * Requires:
   * - `mcporter` installed and on PATH
   * - A configured mcporter server that runs `qmd mcp` with `lifecycle: keep-alive`
   */
  enabled?: boolean;
  /** mcporter server name (defaults to "qmd") */
  serverName?: string;
  /** Start the mcporter daemon automatically (defaults to true when enabled). */
  startDaemon?: boolean;
};

export type MemoryQmdIndexPath = {
  path: string;
  name?: string;
  pattern?: string;
};

export type MemoryQmdSessionConfig = {
  enabled?: boolean;
  exportDir?: string;
  retentionDays?: number;
};

export type MemoryQmdUpdateConfig = {
  interval?: string;
  debounceMs?: number;
  onBoot?: boolean;
  waitForBootSync?: boolean;
  embedInterval?: string;
  commandTimeoutMs?: number;
  updateTimeoutMs?: number;
  embedTimeoutMs?: number;
};

export type MemoryQmdLimitsConfig = {
  maxResults?: number;
  maxSnippetChars?: number;
  maxInjectedChars?: number;
  timeoutMs?: number;
};
